	public override IEnumerator SendInfo(int JJPJHIHBOIB)
	{
		Player targetPlayer = PhotonNetwork.CurrentRoom.GetPlayer(JJPJHIHBOIB, false);
		int numMessages = 0;
		List<HerbGenerator> generators = GeneratorsManager.instance.herbGenerators;
		List<HerbInfo> herbsList = new List<HerbInfo>();
		for (int i = 0; i < generators.Count; i++)
		{
			LoadHerbsMessage loadHerbsMessage = new LoadHerbsMessage();
			loadHerbsMessage.herbsMessage.generatorID = (sbyte)generators[i].ID;
			herbsList.Clear();
			int j = 0;
			List<ForestElement> list = new List<ForestElement>(generators[i].forestElements);
			foreach (Herb item in list)
			{
				if (!((Object)(object)item == (Object)null))
				{
					herbsList.Add(new HerbInfo(item));
					j++;
					if (j >= OnlineManager.LMALNDLJILM.MessageLimits.HerbsPerMessage)
					{
						loadHerbsMessage.herbsMessage.herbs = herbsList.ToArray();
						AHGEFIOOEBL("ReceiveHerbs", targetPlayer, loadHerbsMessage, numMessages++);
						j = 0;
						yield return OnlineLoadManager.LICLEEOILOO;
						loadHerbsMessage = new LoadHerbsMessage();
						loadHerbsMessage.herbsMessage.generatorID = (sbyte)generators[i].ID;
						herbsList.Clear();
					}
				}
			}
			bool kJOOOPJKLBB = i == generators.Count - 1;
			loadHerbsMessage.herbsMessage.herbs = herbsList.ToArray();
			AHGEFIOOEBL("ReceiveHerbs", targetPlayer, loadHerbsMessage, numMessages++, kJOOOPJKLBB);
		}
	}

